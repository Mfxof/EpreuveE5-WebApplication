            <h2>Question 1 : Qu'est-ce que Nextcloud ?</h2>
            <p>Réponse : Nextcloud est une plateforme open-source de collaboration et de partage de fichiers, offrant
                des fonctionnalités similaires à celles de services de cloud comme Dropbox ou Google Drive.</p>

            <h2>Question 2 : Comment puis-je installer Nextcloud sur mon serveur ?</h2>
            <p>Réponse : Vous pouvez installer Nextcloud sur votre propre serveur en suivant les instructions fournies
                sur le site officiel de Nextcloud. Ils proposent des guides d'installation détaillés pour différents
                systèmes d'exploitation.</p>

            <h2>Question 3 : Est-ce que Nextcloud prend en charge le chiffrement des données ?</h2>
            <p>Réponse : Oui, Nextcloud offre des fonctionnalités de chiffrement des données pour assurer la
                confidentialité de vos fichiers. Vous pouvez activer le chiffrement lors de l'installation ou
                ultérieurement dans les paramètres de votre compte.</p>

            <h2>Question 4 : Comment puis-je ajouter des utilisateurs à mon instance Nextcloud ?</h2>
            <p>Réponse : Vous pouvez ajouter des utilisateurs à votre instance Nextcloud en tant qu'administrateur.
                Connectez-vous à votre instance Nextcloud en tant qu'administrateur, accédez aux paramètres
                d'administration, puis recherchez l'option pour ajouter des utilisateurs.</p>

            <h2>Question 5 : Est-ce que Nextcloud offre des applications mobiles ?</h2>
            <p>Réponse : Oui, Nextcloud propose des applications mobiles pour Android et iOS, ce qui vous permet
                d'accéder à vos fichiers et de les synchroniser avec votre appareil mobile.</p>
